public void denda(){
    DateFormat df = new SimpleDateFormat("yyyy-mm-dd");
try{
//Konversi dari string ke tanggal
java.util.Date tanggalpinjam = df.parse(tgl_kembali.getText());
java.util.Date tanggalkembali = df.parse(tf_tglHariIni.getText());

//Tgl di konversi ke milidetik
long Hari1 = tanggalpinjam.getTime();
long Hari2 = tanggalkembali.getTime();
long diff = Hari2 - Hari1;
long Lama = diff / (24 * 60 * 60 * 1000);

tf_telat.setText(Long.toString(Lama));

    keterlambatan=Integer.parseInt(tf_telat.getText());
    //denda=Integer.parseInt(TxtDenda.getText());
    hasil=1000*keterlambatan;       
    if (hasil < 0) {
        hasil = 0;
    }
    tf_denda.setText(String.valueOf(hasil));
} catch (ParseException e){
}
}
