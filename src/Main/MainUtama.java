    private void btn_pinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pinjamActionPerformed
        // TODO add your handling code here:
        String nama = tf_nama.getText();
        String buku = tf_buku.getText();
        String jmlhbuku = tf_jmlhBuku.getText();
    
        int minus = Integer.parseInt(tf_minus_stok.getText());
        int stok = Integer.parseInt(tf_jmlhBuku.getText());
        
        if (tf_nama.getText().length()==0) {
                JOptionPane.showMessageDialog(this, "kolom kosong");
                }else if(tf_buku.getText().length()==0) {
                    JOptionPane.showMessageDialog(this, "tidak ada buku yang dipilih!!");            
                }else if(minus < stok){
                    JOptionPane.showMessageDialog(this, "Stok tidak mencukupi!!!");
                }
            else{
                try {
                    //tanggal
                String frmt = "yyyy-MM-dd";
                SimpleDateFormat sdf = new SimpleDateFormat(frmt);
                String pinjam = String.valueOf(sdf.format(tglpinjam.getDate()));
                String kembali = String.valueOf(sdf.format(tglkmbl.getDate()));
                    String sql = "insert into pinjam (nama,kelas,buku,jmlhbuku,tglPinjam,tglKmbl) values (?,?,?,?,?,?)";
                    PreparedStatement ps = conn.prepareStatement(sql);
                    ps.setString(1, nama);
                    ps.setString(2, lbl_kelaspinjam.getText());
                    ps.setString(3, buku);
                    ps.setString(4, jmlhbuku);
                    ps.setString(5, pinjam);
                    ps.setString(6, kembali);
                ps.executeUpdate();
                try {
                    String sql2 = "insert into history (nama,kelas,buku,jmlhbuku,tglPinjam,tglKmbl) values (?,?,?,?,?,?)";
                        PreparedStatement ps2 = conn.prepareStatement(sql2);
                        ps2.setString(1, nama);
                        ps2.setString(2, lbl_kelaspinjam.getText());
                        ps2.setString(3, buku);
                        ps2.setString(4, jmlhbuku);
                        ps2.setString(5, pinjam);
                        ps2.setString(6, kembali);
                    ps2.executeUpdate();
            } catch (SQLException e) {
            }
        model.setRowCount(0);
        getDatabuku();
        model2.setRowCount(0);
        getDataRekap();
        bersih();
        JOptionPane.showMessageDialog(this, "Berhasil Meminjam Buku >.<");
        pindah();
        } catch (HeadlessException | SQLException e) {
        JOptionPane.showMessageDialog(this, "Data Gagal ditambahkan "+e);
        }
                }
        
    }//GEN-LAST:event_btn_pinjamActionPerformed

